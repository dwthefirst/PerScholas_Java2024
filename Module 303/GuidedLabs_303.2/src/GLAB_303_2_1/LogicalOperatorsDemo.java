package GLAB_303_2_1;

public class LogicalOperatorsDemo {
    public static void main(String[] args) {
        /*
            LOGICAL OPERATORS
         */
        boolean x = true;
        boolean y = false;
        System.out.println("x = " + x);
        System.out.println("y = " + y);

        System.out.println("x & y : " + (x & y));
        System.out.println("x && y : " + (x && y));
        System.out.println("x | y : " + (x | y));
        System.out.println("x || y: " + (x || y));
        System.out.println("x ^ y : " + (x ^ y));
        System.out.println("!x : " + (!x));

    }
}
